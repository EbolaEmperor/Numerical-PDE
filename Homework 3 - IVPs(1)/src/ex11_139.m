B = [ -1/12, 2/3, 5/12, 0, 0; 1/24, -5/24, 19/24, 3/8, 0; -19/720, 53/360, -11/30, 323/360, 251/720 ];
for s = 1:3
    b = B(s,:);
    rho = @(x)(x.^(s+1)-x.^(s));
    sigma = @(x)(b(1)+b(2)*x+b(3)*(x.^2)+b(4)*(x.^3)+b(5)*(x.^4));
    X = 0:0.01:2*pi;
    Y = rho(exp(1i*X))./sigma(exp(1i*X));
    plot(real(Y),imag(Y));
    xlim([-7,1]);
    ylim([-3.3,3.3]);
    hold on
end